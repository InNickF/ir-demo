Cypress.Commands.add("login" as never, () => {
  const username = Cypress.env("CYPRESS_USERNAME");
  const password = Cypress.env("CYPRESS_USERNAME_PASSWORD");
  cy.visit("/auth");
  cy.get('[id="email"]').type(username);
  cy.get('[id="password"]').type(password);
  cy.get('[data-cy="auth-login-button"]').click();
});

